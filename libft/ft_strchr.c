char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*p;

	i = 0;
	p = (char *)s;
	while ((s[i] != (char)c) && s[i] != '\0')
	{
		i++;
	}
	if (s[i] == (char)c)
	{
		return (p += i);
	}
	else
	{
		return (0);
	}
}
