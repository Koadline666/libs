#include "../libft.h"

int	ft_lstlen(t_list *lst)
{
	int		i;
	t_list	*ptr;

	i = 0;
	ptr = lst;
	while (ptr != NULL)
	{
		ptr = ptr -> next;
		i++;
	}
	return (i);
}
